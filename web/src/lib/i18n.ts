type TranslationKey = keyof typeof translations['en'];

function getLanguage(): Language {
  if (typeof window === 'undefined') return 'en';
  const stored = localStorage.getItem(STORAGE_KEY);
  if (stored === 'en' || stored === 'hi' || stored === 'sd') return stored;
  return 'en';
}

function translate(lang: Language, key: string): string {
  const dict = translations[lang] as Record<string, string>;
  if (key in dict) return dict[key];
  // Fallback to English
  const en = translations.en as Record<string, string>;
  if (key in en) return en[key];
  return key;
}

export function setLanguage(code: Language) {
  if (typeof window !== 'undefined') {
    localStorage.setItem(STORAGE_KEY, code);
    window.dispatchEvent(new CustomEvent('lang-change', { detail: code }));
  }
}

export function useTranslation() {
  const [lang, setLang] = useState<Language>(getLanguage);

  useEffect(() => {
    const handleChange = () => setLang(getLanguage());
    const handleStorage = (e: StorageEvent) => {
      if (e.key === STORAGE_KEY) handleChange();
    };
    window.addEventListener('storage', handleStorage);
    window.addEventListener('lang-change', handleChange);
    return () => {
      window.removeEventListener('storage', handleStorage);
      window.removeEventListener('lang-change', handleChange);
    };
  }, []);

  const t = useCallback(
    (key: string): string => translate(lang, key),
    [lang],
  );

  return { t, lang };
}
